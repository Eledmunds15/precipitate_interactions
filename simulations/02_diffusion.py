"""
sim_framework.py

Molecular Dynamics (MD) Simulation Framework for Stress-Controlled Diffusion.

PURPOSE:
    This script initializes and executes a 'rerun' simulation where a 
    specific configuration of Self-Interstitial Atoms (SIAs), extracted 
    from a previous analysis, is evolved under specific temperature and 
    stress conditions.

    It handles the path mapping between a local workstation (analysis) 
    and an HPC cluster (execution), ensuring that even if the restart 
    files are missing locally, the metadata correctly points to the 
    HPC scratch space.

WORKFLOW ALIGNMENT:
    1. Source Data: Always pulled from T800 simulations (via metadata.json).
    2. Target Data: Can be T700, T800, or T900 as specified in metadata.
    3. Input: A JSON metadata file generated by the pre-processing script.

USAGE:
    # 1. DRY RUN (Local Testing)
    # Checks path logic and metadata parsing without calling LAMMPS.
    python /home/Ethan/Projects/prec_interactions/simulations/02_diffusion.py --meta ~/Projects/prec_interactions/data/rerun/R30_S150_T700/metadata.json

    mpirun -np 16 python /home/Ethan/Projects/prec_interactions/simulations/02_diffusion.py --meta ~/Projects/prec_interactions/data/rerun/R30_S150_T700/metadata.json --bench 0

    # 2. FULL HPC RUN (Slurm/MPI)
    # Uses the HPC paths defined in the JSON to locate the .restart file.
    mpirun -np 128 python sim_framework.py --meta /mnt/parscratch/users/mtp24ele/private/prec_interactions/data/rerun/R30_S150_T700/metadata.json

ARGUMENTS:
    --meta      : (str) Path to the metadata.json file for the specific rerun case.
    --bench     : (int) Optional benchmark mode (0 or 1).
    --run_time  : (int) Optional override for total timesteps (default: 1,500,000).

ENVIRONMENT LOGIC:
    - If running on local path (/home/Ethan), the script references 'RestartFile_local'.
    - If running elsewhere (HPC), it references 'RestartFile_HPC'.
    - Results are written to: .../data/rerun_results/diffusion_T{temp}_ST{stress}_R{radius}_N{seed}/
"""
import numpy as np
import json
import yaml
import shutil
import argparse
from pathlib import Path
from dataclasses import dataclass, field, asdict
from mpi4py import MPI

@dataclass
class SimParams:
    radius: int
    target_stress: float
    step: int
    temperature: int
    restart_file_local: Path
    restart_file_HPC: Path
    num_sias: int
    box_bounds: dict
    atoms_file_local: Path
    atoms_file_HPC: Path
    original_sim_dir: Path
    bench: int = None
    thermo_time: int = 10_000
    run_time: int = 1_500_000
    dt: float = 0.001
    species: str = "Fe"
    potential_path: Path = Path("potentials/mendelev03.fs")
    thermo_freq: int = 100
    dump_freq: int = 1000
    restart_freq: int = 10_000
    num_cores: int = field(default=1)
    random_seed: int = field(default_factory=lambda: 1234)

    def __post_init__(self):

        # 2. Benchmarking Logic: Override run times
        if self.bench == 0:
            self.thermo_time = 1
            self.run_time = 1
        elif self.bench == 1:
            self.thermo_time = 500
            self.run_time = 500

    @property
    def case_name(self) -> str:
        # Fixed: cast target_stress to int to avoid ST150.0 in folder names
        return f"diffusion_T{self.temperature}_ST{int(self.target_stress)}_R{self.radius}_N{self.random_seed}"

def load_metadata(file_path: Path) -> dict:
    file_path = Path(file_path)
    if not file_path.exists():
        raise FileNotFoundError(f"Metadata file not found: {file_path}")
        
    if file_path.suffix in [".yaml", ".yml"]:
        with open(file_path) as f:
            return yaml.safe_load(f)
    elif file_path.suffix == ".json":
        with open(file_path) as f:
            return json.load(f)
    raise ValueError(f"Unsupported metadata format: {file_path.suffix}")

def parse_arguments() -> SimParams:
    parser = argparse.ArgumentParser(description="MD simulation runner")
    parser.add_argument("--meta", type=str, required=True, help="Path to metadata JSON/YAML")
    parser.add_argument("--bench", type=int, choices=[0, 1], default=None)
    parser.add_argument("--run_time", type=int, default=None)
    args = parser.parse_args()

    meta = load_metadata(args.meta)
    
    # Map JSON keys exactly as they were saved in the previous script
    return SimParams(
        radius=meta["Radius"],
        target_stress=meta["TargetStress"],
        step=meta["Step"],
        temperature=meta["Temperature"],
        restart_file_local=Path(meta["RestartFile_local"]),
        restart_file_HPC=Path(meta["RestartFile_HPC"]),
        atoms_file_local=Path(meta["AtomsFile_local"]),
        atoms_file_HPC=Path(meta["AtomsFile_HPC"]),
        num_sias=meta["NumSIAs"],
        box_bounds=meta["BoxBounds"],
        original_sim_dir=Path(meta["OriginalSimDir"]),
        bench=args.bench,
        run_time=args.run_time if args.run_time is not None else 1_500_000,
    )

def init_paths(params: SimParams) -> dict:
    base = params.original_sim_dir.parent / params.case_name
    
    paths = {
        "base": base,
        "logs": base / "logs",
        "dump": base / "dump",
        "restart": base / "restart",
        "output": base / "output",
        "inputs": base / "inputs",
        "metadata": base / "run_config.yaml"
    }
    
    # Create directories
    for p in paths.values():
        if not p.suffix:
            p.mkdir(parents=True, exist_ok=True)

    # Determine which source files to use based on where we are running
    if "/home/Ethan" in str(Path.home()):
        src_restart = params.restart_file_local
        src_atoms = params.atoms_file_local
    else:
        src_restart = params.restart_file_HPC
        src_atoms = params.atoms_file_HPC

    # Copy files into the simulation's local inputs directory
    if src_restart.exists():
        shutil.copy(src_restart, paths["inputs"] / "input.restart")
    else:
        print(f"Warning: Restart source {src_restart} not found!")

    if src_atoms.exists():
        shutil.copy(src_atoms, paths["inputs"] / "atoms.txt")
    else:
        print(f"Warning: Atoms source {src_atoms} not found!")

    return paths

def populate(lmp, filepath):
    """
    Reads coordinates from a text file and injects them as Type 1 atoms 
    into the existing LAMMPS simulation.
    """
    # Load coordinates using numpy (handles the whitespace/lines automatically)
    coords = np.loadtxt(filepath)
    
    print(f"--- Populating {len(coords)} SIAs into the simulation ---")
    
    for row in coords:
        x, y, z = row
        # Command: create_atoms <type> single <x> <y> <z>
        # Note: We use f-strings to pass the coordinates directly to lmp.cmd
        lmp.cmd.create_atoms(1, "single", x, y, z, "group", "siagrp")
    
    # Crucial: Re-evaluate the group 'mobgrp' so it includes the new atoms
    # If the group was defined by 'type', this simple line refreshes it.
    lmp.cmd.group("mobgrp", "type", 1)
    lmp.cmd.group("all", "union", "fixgrp", "mobgrp")


def run_simulation(params: SimParams, paths: dict, comm, dry_run=True) -> None:
    # Choose which restart path to use: 
    # If we are on your laptop (Ethan), use local. If on HPC, use HPC path.
    if "/home/Ethan" in str(Path.home()):
        actual_restart = params.restart_file_local
    else:
        actual_restart = params.restart_file_HPC

    if dry_run:
        print("\n=== DRY RUN MODE ===")
        print(f"Target Case: {params.case_name}")
        print(f"Source Restart: {actual_restart}")
        print(f"Output Dir: {paths['base']}")
        print("====================\n")
        return
    
    if "/home/Ethan" in str(Path.home()):
        actual_atoms = params.atoms_file_local
    else:
        actual_atoms = params.atoms_file_HPC

    # LAMMPS execution logic...
    from lammps import lammps
    lmp = lammps(comm=comm)
    
    lmp.cmd.log(paths["logs"] / "setup.log")
    
    lmp.cmd.processors("*", 2, "*")
    lmp.cmd.units("metal")
    lmp.cmd.dimension(3)
    lmp.cmd.boundary("p", "s", "p") # Set shrink-wrapped boundaries along the y-direction
    lmp.cmd.atom_style("atomic")
    lmp.cmd.atom_modify("map", "yes")
    lmp.cmd.timestep(params.dt)

    lmp.cmd.read_restart(str(actual_restart))

    # ===========================
    # Define the interatomic potential
    # ===========================
    lmp.cmd.pair_style("eam/fs")
    lmp.cmd.pair_coeff("* *", params.potential_path, params.species, params.species, params.species, params.species)

    lmp.cmd.neighbor(2.0, "bin")
    lmp.cmd.neigh_modify("delay", 10, "check", "yes")

    # ===========================
    # Define groups
    # ===========================
    lmp.cmd.group("mobgrp", "type", "1")
    lmp.cmd.group("topgrp", "type", "2")
    lmp.cmd.group("botgrp", "type", "3")
    lmp.cmd.group("precgrp", "type", "4")
    lmp.cmd.group("fixgrp", "union", "precgrp", "topgrp", "botgrp")
    lmp.cmd.group("all", "union", "fixgrp", "mobgrp")

    # ===========================
    # Computes
    # ===========================
    lmp.cmd.compute("stress", "all", "stress/atom", "NULL")
    lmp.cmd.compute("mobtemp", "mobgrp", "temp")
    lmp.cmd.compute("pe", "all", "pe/atom")
    lmp.cmd.compute("ke", "all", "ke/atom")

    lmp.cmd.compute("mobstressXX", "mobgrp", "reduce", "sum", "c_stress[1]")
    lmp.cmd.compute("mobstressYY", "mobgrp", "reduce", "sum", "c_stress[2]")
    lmp.cmd.compute("mobstressZZ", "mobgrp", "reduce", "sum", "c_stress[3]")
    lmp.cmd.compute("mobstressXY", "mobgrp", "reduce", "sum", "c_stress[4]")
    lmp.cmd.compute("mobstressYZ", "mobgrp", "reduce", "sum", "c_stress[5]")
    lmp.cmd.compute("mobstressXZ", "mobgrp", "reduce", "sum", "c_stress[6]")

    lmp.cmd.compute("mobpetot", "mobgrp", "reduce", "sum", "c_pe")
    lmp.cmd.compute("mobketot", "mobgrp", "reduce", "sum", "c_ke")

    # ===========================
    # Minimize
    # ===========================
    lmp.cmd.fix(1, "fixgrp", "setforce", 0.0, 0.0, 0.0)

    lmp.cmd.reset_timestep(0)
    lmp.cmd.thermo_style("custom", "step", "temp", "pe", "etotal", "pxx", "pyy", "pzz", "pxy", "pyz", "pxz", "lx", "ly", "lz")
    lmp.cmd.thermo_modify("temp", "mobtemp")
    lmp.cmd.thermo(1)

    lmp.cmd.min_style("cg")
    lmp.cmd.minimize(1e-4, 1e-6, 5000, 10000)

    lmp.cmd.write_dump(
        "all", "custom", paths["output"] / "minimized_initial.dump",
        "id", "type", "x", "y", "z",
        "c_pe", "c_ke",
        "c_stress[1]", "c_stress[2]", "c_stress[3]", "c_stress[4]", "c_stress[5]", "c_stress[6]",
    )

    # ===========================
    # Populate
    # ===========================
    populate(lmp, actual_atoms)   

    # ===========================
    # Minimize with SIAs
    # ===========================
    lmp.cmd.reset_timestep(0)

    lmp.cmd.min_style("fire")
    lmp.cmd.minimize(1e-4, 1e-6, 2000, 10000)

    lmp.cmd.min_style("cg")
    lmp.cmd.minimize(1e-4, 1e-6, 5000, 10000)

    lmp.cmd.write_dump(
        "all", "custom", paths["output"] / "minimized_final.dump",
        "id", "type", "x", "y", "z",
        "c_pe", "c_ke",
        "c_stress[1]", "c_stress[2]", "c_stress[3]", "c_stress[4]", "c_stress[5]", "c_stress[6]",
    )

    # ===========================
    # Setup Outputs
    # ===========================
    lmp.cmd.reset_timestep(0)
    lmp.cmd.log(paths["logs"] / "diffusion.log")

    lmp.cmd.thermo(params.thermo_freq)

    lmp.cmd.thermo_style(
        "custom", "step", "temp", "pe", "etotal", 
        "pxx", "pyy", "pzz", "pxy", "pyz", "pxz",
        "c_mobpetot", "c_mobketot", "c_mobtemp",
        "c_mobstressXX", "c_mobstressYY", "c_mobstressZZ",
        "c_mobstressXY", "c_mobstressYZ", "c_mobstressXZ"
    )
    lmp.cmd.thermo_modify("temp", "mobtemp")

    lmp.cmd.dump("dump", "all", "custom", params.dump_freq, paths["dump"] / "dump_*.lammpstrj", 
                 "id", "type", "x", "y", "z", "vx", "vy", "vz", "fx", "fy", "fz", "c_pe", "c_ke", "c_stress[1]", "c_stress[2]", "c_stress[3]", "c_stress[4]", "c_stress[5]", "c_stress[6]"
                 )
    
    lmp.cmd.restart(params.restart_freq, paths["restart"] / "*.restart")

    # ===========================
    # Ramping Temperature
    # ===========================
    lmp.cmd.velocity("mobgrp", "create", 10.0, params.random_seed, "mom", "yes", "rot", "yes", "dist", "gaussian")
    lmp.cmd.fix(2, "mobgrp", "nvt", "temp", 10.0, params.temperature, params.dt*10)
    lmp.cmd.fix_modify(2, "temp", "mobtemp")

    lmp.cmd.run(params.thermo_time)
    lmp.cmd.unfix(2)

    # ===========================
    # Holding Temperature
    # ===========================
    lmp.cmd.fix(2, "mobgrp", "nvt", "temp", params.temperature, params.temperature, params.dt*100)
    lmp.cmd.fix_modify(2, "temp", "mobtemp")

    lmp.cmd.run(params.run_time)

def main():
    comm = MPI.COMM_WORLD
    rank = comm.Get_rank()
    
    params = parse_arguments()
    params.num_cores = comm.Get_size()
    
    paths = init_paths(params)
    
    if rank == 0:
        # Save a copy of the finalized parameters used for this specific run
        meta_dict = asdict(params)
        for k, v in meta_dict.items():
            if isinstance(v, Path): meta_dict[k] = str(v)
        with open(paths["metadata"], "w") as f:
            yaml.dump(meta_dict, f)

    run_simulation(params, paths, comm, dry_run=False)

if __name__ == "__main__":
    main()